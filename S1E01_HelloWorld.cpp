//     Создаётся проект без файлов
//     Добавляем в проект файл Main.cpp (имя любое):
//       Вызываем контекстное меню в Solution Explorer на элементе Source Files
//       Context menu\Add\New Item...\Visual C++\С++ File (.cpp)
//       В поле Name пишем имя файла (Main.cpp).
//       Жмём Add
//     Работаем в Main.cpp
// Пишем приложение Hello World
//   Код:
//     #include <iostream>
//     void main()
//     {
//       std::cout << "Hello";
//     }
//   Объясняем строчки:
//     #include
//     Функция main
//       void - возвращаемое значение (ничего)
//       () - входные аргументы (ничего)
//       {...} - тело
//     std::cout
//       Всё, что нужно пока понимать, что выводить в консоль нужно так:
//         std::cout << что-то;
//       Пока это просто магическая строчка.
//       Но если кратко, то:
//         cout - глобальный объект потока вывода (без деталей, потом)
//         std:: - namespace стандартной библиотеки C++ (без деталей, потом)
//     "Hello" - строка (без деталей, потом)
//     ; - конец инструкции (statement)
//   Запускаем из студии (F5) - сразу завершается
//   Зупускаем под отладкой
//     Выставляем breakpoint (F9) после cout
//     Запускаем (F5)
//     Показываем консоль
//     Завершаем (F5)
//   Показываем exe-файл
//     <Папка пректа>\Debug\<Имя проекта>.cpp
//     Запускаем его двойным кликом
